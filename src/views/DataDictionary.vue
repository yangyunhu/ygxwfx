      isEditGroup: false,
      groupForm: {
        dictCode: '',
        dictName: '',
        dictType: 'sys',
        status: '1',
        remark: '',
      },
      groupRules: {
        dictCode: [
          { required: true, message: '请输入字典编码', trigger: 'blur' },
          { pattern: /^[a-zA-Z_]+$/, message: '字典编码只能包含英文字母和下划线', trigger: 'blur' }
        ],
        dictName: [
          { required: true, message: '请输入字典名称', trigger: 'blur' }
        ],
        dictType: [
          { required: true, message: '请选择字典类型', trigger: 'change' }
        ],
      },
      
      // 字典项表单
