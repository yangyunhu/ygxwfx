            },
          ],
        };

        this.dictItems = mockItems[dictCode] || [];
        this.loading = false;
      }, 300);
    },
    // 新增字典组
    handleAddDict() {
      this.isEditGroup = false;
      this.groupDialogVisible = true;
    },
    // 编辑字典组
    handleEditGroup() {
      this.isEditGroup = true;
      Object.assign(this.groupForm, this.selectedGroup);
      this.groupDialogVisible = true;
    },
    // 新增字典项
    handleAddItem() {
      this.isEditItem = false;
      this.itemDialogVisible = true;
    },
    // 编辑字典项
    handleEditItem(row) {
      this.isEditItem = true;
      Object.assign(this.itemForm, row);
      this.itemDialogVisible = true;
    },
    // 删除字典项
    handleDeleteItem(row) {
      this.$confirm(`确定要删除字典项"${row.itemName}"吗？`, "提示", {
        confirmButtonText: "确定",
        cancelButtonText: "取消",
        type: "warning",
      }).then(() => {
        this.$message.success("删除成功");
      }).catch(() => {});
    },
    
    // 提交字典组表单
    submitGroupForm() {
      this.$refs.groupForm.validate((valid) => {
        if (valid) {
          if (this.isEditGroup) {
            // 编辑逻辑
            Object.assign(this.selectedGroup, this.groupForm);
            this.$message.success('字典组更新成功');
          } else {
            // 新增逻辑
            const newGroup = {
              ...this.groupForm,
              itemCount: 0,
              createTime: new Date().toLocaleString(),
            };
            this.dictGroups.push(newGroup);
            this.$message.success('字典组新增成功');
            // 自动选中新新增的字典组
            this.selectGroup(newGroup);
          }
          this.groupDialogVisible = false;
        }
      });
    },
    
    // 提交字典项表单
    submitItemForm() {
      this.$refs.itemForm.validate((valid) => {
        if (valid) {
          if (this.isEditItem) {
            // 编辑逻辑
            const index = this.dictItems.findIndex(item => item.itemCode === this.itemForm.itemCode);
            if (index > -1) {
              Object.assign(this.dictItems[index], this.itemForm);
            }
            this.$message.success('字典项更新成功');
          } else {
            // 新增逻辑
            const newItem = {
              ...this.itemForm,
            };
            this.dictItems.push(newItem);
            // 更新字典组的项数量
            if (this.selectedGroup) {
              this.selectedGroup.itemCount = this.dictItems.length;
            }
            this.$message.success('字典项新增成功');
          }
          this.itemDialogVisible = false;
        }
      });
    },
    
    // 重置字典组表单
    resetGroupForm() {
      this.$refs.groupForm && this.$refs.groupForm.resetFields();
      this.groupForm = {
        dictCode: '',
        dictName: '',
        dictType: 'sys',
        status: '1',
        remark: '',
      };
    },
    
    // 重置字典项表单
    resetItemForm() {
      this.$refs.itemForm && this.$refs.itemForm.resetFields();
      this.itemForm = {
        itemCode: '',
        itemName: '',
        itemValue: '',
        sort: 1,
        status: '1',
        remark: '',
      };
    },
    
    // 自动生成字典编码
    autoGenerateDictCode() {
      if (!this.groupForm.dictName || this.isEditGroup) {
        return;
      }
      
      // 常见中文词汇到英文的映射
      const commonWords = {
        '用户': 'user',
        '类型': 'type',
        '性别': 'gender',
        '组织': 'org',
        '级别': 'level',
        '状态': 'status',
        '角色': 'role',
        '部门': 'dept',
        '岗位': 'position',
        '数据': 'data',
        '范围': 'range',
        '配置': 'config',
        '管理': 'management',
        '权限': 'permission',
        '系统': 'system',
        '业务': 'business',
        '考勤': 'attendance',
        '员工': 'employee',
        '人员': 'personnel',
        '日志': 'log',
        '菜单': 'menu',
        '模块': 'module',
        '功能': 'function',
      };
      
      let name = this.groupForm.dictName;
      let code = '';
      
      // 尝试匹配常见词汇
      let matched = false;
      for (let [chinese, english] of Object.entries(commonWords)) {
        if (name.includes(chinese)) {
          code = code ? `${code}_${english}` : english;
          name = name.replace(chinese, '');
          matched = true;
        }
      }
      
      // 如果没有匹配到任何词汇，使用拼音首字母
      if (!matched || code === '') {
        // 简单的中文转拼音首字母（仅支持常见汉字）
        const pinyinMap = {
          '一': 'yi', '二': 'er', '三': 'san', '四': 'si', '五': 'wu',
          '六': 'liu', '七': 'qi', '八': 'ba', '九': 'jiu', '十': 'shi',
        };
        
        // 如果包含中文，使用拼音首字母组合
        const chineseChars = name.match(/[\u4e00-\u9fa5]/g);
        if (chineseChars) {
          const pinyinFirst = chineseChars.map(char => {
            return pinyinMap[char] || char.charCodeAt(0).toString(36);
          }).join('');
          code = code ? `${code}_${pinyinFirst}` : pinyinFirst;
        } else {
          // 如果是英文，直接转小写并替换空格
          code = name.toLowerCase().replace(/\s+/g, '_');
        }
      }
      
      // 清理编码：只保留字母、数字和下划线
      code = code.replace(/[^a-zA-Z0-9_]/g, '_');
      // 移除连续的下划线
      code = code.replace(/_+/g, '_');
      // 移除开头和结尾的下划线
      code = code.replace(/^_+|_+$/g, '');
      // 转为小写
      code = code.toLowerCase();
      
      // 如果生成的编码为空，使用默认值
      if (!code) {
        code = 'dict_' + Date.now().toString(36);
      }
      
      // 检查编码是否已存在，如果存在则添加数字后缀
      const existingCodes = this.dictGroups.map(g => g.dictCode);
      let finalCode = code;
      let suffix = 1;
      while (existingCodes.includes(finalCode)) {
        finalCode = `${code}_${suffix}`;
        suffix++;
      }
      
      this.groupForm.dictCode = finalCode;
    },
    
    // 自动生成字典项编码
    autoGenerateItemCode() {
      if (!this.itemForm.itemName || this.isEditItem) {
        return;
      }
      
      // 获取当前字典组的编码作为前缀
      const groupPrefix = this.selectedGroup ? this.selectedGroup.dictCode : 'item';
      
      // 常见中文词汇到英文的映射
      const commonWords = {
        '管理员': 'admin',
        '普通': 'normal',
        '用户': 'user',
        '访客': 'guest',
        '男': 'male',
        '女': 'female',
        '省级': 'province',
        '市级': 'city',
        '县级': 'county',
        '单位': 'unit',
        '启用': 'enable',
        '禁用': 'disable',
        '是': 'yes',
        '否': 'no',
        '成功': 'success',
        '失败': 'fail',
        '通过': 'pass',
        '拒绝': 'reject',
        '待审核': 'pending',
        '已审核': 'approved',
      };
      
      let name = this.itemForm.itemName;
      let suffix = '';
      
      // 尝试匹配常见词汇
      let matched = false;
      for (let [chinese, english] of Object.entries(commonWords)) {
        if (name.includes(chinese)) {
          suffix = suffix ? `${suffix}_${english}` : english;
          name = name.replace(chinese, '');
          matched = true;
        }
      }
      
      // 如果没有匹配到任何词汇，使用拼音
      if (!matched || suffix === '') {
        // 简单的中文转拼音（仅支持常见汉字）
        const pinyinMap = {
          '一': 'yi', '二': 'er', '三': 'san', '四': 'si', '五': 'wu',
          '六': 'liu', '七': 'qi', '八': 'ba', '九': 'jiu', '十': 'shi',
        };
        
        // 如果包含中文，使用拼音
        const chineseChars = name.match(/[\u4e00-\u9fa5]/g);
        if (chineseChars) {
          const pinyinStr = chineseChars.map(char => {
            return pinyinMap[char] || char.charCodeAt(0).toString(36);
          }).join('');
          suffix = suffix ? `${suffix}_${pinyinStr}` : pinyinStr;
        } else {
          // 如果是英文，直接转小写并替换空格
          suffix = name.toLowerCase().replace(/\s+/g, '_');
        }
      }
      
      // 组合编码：字典组编码_字典项编码
      let code = suffix ? `${groupPrefix}_${suffix}` : groupPrefix;
      
      // 清理编码：只保留字母、数字和下划线
      code = code.replace(/[^a-zA-Z0-9_]/g, '_');
      // 移除连续的下划线
      code = code.replace(/_+/g, '_');
      // 移除开头和结尾的下划线
      code = code.replace(/^_+|_+$/g, '');
      // 转为小写
      code = code.toLowerCase();
      
      // 如果生成的编码为空，使用时间戳
      if (!code) {
        code = `${groupPrefix}_${Date.now().toString(36)}`;
      }
      
      // 检查编码是否已存在，如果存在则添加数字后缀
      const existingCodes = this.dictItems.map(item => item.itemCode);
      let finalCode = code;
      let numSuffix = 1;
      while (existingCodes.includes(finalCode)) {
        finalCode = `${code}_${numSuffix}`;
        numSuffix++;
      }
      
      this.itemForm.itemCode = finalCode;
    },
  },
};
</script>

