            itemCount: 3,
          },
          {
            dictCode: "gender",
            dictName: "性别",
            dictType: "sys",
            status: "1",
            remark: "用户性别字典",
            createTime: "2024-01-02 10:00:00",
            itemCount: 2,
          },
          {
            dictCode: "org_level",
            dictName: "组织级别",
            dictType: "sys",
            status: "1",
            remark: "组织机构级别字典",
            createTime: "2024-01-03 10:00:00",
            itemCount: 4,
          },
        ];

        // 默认选中第一个
        if (this.dictGroups.length > 0) {
          this.selectGroup(this.dictGroups[0]);
        }

        this.loading = false;
      }, 500);
    },
    // 选择字典组
    selectGroup(group) {
      this.selectedGroup = group;
      this.page = 1;
      this.loadDictItems(group.dictCode);
    },
    // 加载字典项数据
    loadDictItems(dictCode) {
      this.loading = true;
      // TODO: 根据 dictCode 从后端加载字典项
      setTimeout(() => {
        // 示例数据
        const mockItems = {
